    private void populateDoctor() {
        
        model = (DefaultTableModel) tblDoctor.getModel();
        model.setRowCount(0);
        Object[] row = new Object[5];
        for(Doctor d : docDirectory.getDocDirectory())
        {   
            row[0] = d.getDocName();
            row[1] = d.getDocId();
            row[2] = d.getDepartment();
            row[3] = d.getUsername();
            row[4] = d.getPassword();
            
            model.addRow(row);
        }
    }
